package ch02;

public class Ex05_bool {
	public static void main(String[] args) {
		// 논리형, true 참, false 거짓
		boolean b = true;
		System.out.println(b);
		System.out.println(!b);
		System.out.println(10 > 5);
		System.out.println(10 < 5);
		// A = B <= B의 값을 A에 대입, assign
		// A == B <= equal
		// A != B <= 같지 않다. not equal
		System.out.println(b == false);
		System.out.println(b != true);
	}
}

package ch02;

public class Test {
	static int c;
	
	
	// 함수, method
	public static void main(String[] args) {
		// 자료형 변수;
		int a;	// 변수 선언
		a = 10;	// 변수 a에 10을 대입(초기화)
		// 10 = a; // 상수는 좌변에 올 수 없음 
		// 좌변 = 우변
		System.out.println(a);
		a = 20;
		System.out.println(a);
		
		char b; // 문자형 변수 선언
		b = 'A'; // 작은 따옴표로 묶음
		System.out.println(b);
		
		System.out.println(c);
	}
}

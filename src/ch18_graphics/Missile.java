package ch18_graphics;

// 게임에 쓰일 미사일 클래스
public class Missile {
	private int mx;	// x 좌표 
	private int my;	// y 좌표
	
	// getters and setters
	public int getMx() {
		return mx;
	}
	public void setMx(int mx) {
		this.mx = mx;
	}
	public int getMy() {
		return my;
	}
	public void setMy(int my) {
		this.my = my;
	}
}
